using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Artillery : Unit
{
    public Artillery(PlayerTag playerTag, Color playerColour)
    {
        this.playerTag = playerTag;
        this.playerColour = playerColour;
        name = "Artillery";
        buildTime = 15;

        resourceCost.food = 0;
        resourceCost.wood = 100;
        resourceCost.stone = 0;
        resourceCost.gold = 100;

        health = 100;

        damage.damageToCitizen = 100;
        damage.damageToSoldiers = 99;
        damage.damageToCavalier = 40;
        damage.damageToArtillery = 99;
        damage.damageToBuildings = 30;
    }
}
